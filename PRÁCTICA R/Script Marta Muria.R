# Hacemos un boxplot con los datos transformados. ¿Qué significan los parámetros que hemos empleado? #significan: colores=col, titulo=main, ejes en vetical=las
# ¿Qué es un boxplot? #Es un diagrama de cajas y bigotes. Es una representación gráfica que se utiliza en estadística para visualizar la distribución y resumir estadísticas importantes de un conjunto de datos numéricos.
