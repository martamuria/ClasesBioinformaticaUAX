#######################################
# Análisis de Expresión Diferencial 
#######################################
#Vamos a separar los wildtype de los knockout
# Primero separamos las dos condiciones. ¿Qué tipo de datos has generado? #Hemos generado una tabla/matrix
wt <- data[,1:3]
ko <- data[,4:6]
class(wt) 
head(wt) 
#class nos dice la clase de datos que tenemos, si es una matrix, una linea, etc

# Calcula las medias de las muestras para cada condición. Usa apply #calcula donde yo quiera la funcion que yo quiera
wt.mean = apply(wt, 1, mean)
head(wt.mean)
ko.mean = apply(ko, 1, mean)
head(ko.mean)
#el 1 se usa para cada fila y el 2 para cada columna

# ¿Cuál es la media más alta? #es mas alta la de los ko
max(wt.mean)
max(ko.mean)

# Ahora hacemos un scatter plot (gráfico de dispersión) #un tipo de grafico de puntos que empiezan en un nucelo y se van abriendo
plot(ko.mean ~ wt.mean)

plot(ko.mean ~ wt.mean,xlab = "WT", ylab = "KO", main="GSE5583 - Grafico de dispersion")

# Añadir una línea diagonal con abline
#importante hacerlo cuando el plot está abierto #El 0 y el 1 son los ejes y=ax+b. b será 0 y a será 1. Por tanto y=1x
abline(0,1,col="red")
abline(h=2,col="blue")
abline(v=5,col="green")
#ponemos h para poner una recta horizontal y v para una recta vertical

# ¿Eres capaz de añadirle un grid? #si, se usa para añadirle una cuadricula
grid()
# Calculamos la diferencia entre las medias de las condiciones #las condiciones son wt y ko
diff.mean = wt.mean - ko.mean
diff.mean

# Hacemos un histograma de las diferencias de medias
hist(diff.mean,col="pink")

# Calculamos la significancia estadística con un t-test. #de los t-test nos interesa el pvalue
# Primero crea una lista vacía para guardar los p-values
# Segundo crea una lista vacía para guardar las estadísticas del test.
# OJO que aquí usamos los datos SIN TRANSFORMAR. ¿Por qué? para que sean datos fiables sin manipular.
# ¿Cuántas valores tiene cada muestra? #Dentro de cada condición hay 3 valores, es decir 3 muestras.

pvalue = NULL
tstat = NULL
for(i in 1 : nrow(data)) { #Para cada gen
	x = wt[i,] # gene wt número i
	y = ko[i,] # gene ko número i

	#Hacemos el test
	t = t.test(x, y)

	#Añadimos el p-value a la lista
	pvalue[i] = t$p.value
	#Añadimos las estadísticas a la lista
	tstat[i] = t$statistic
}
head(pvalue)
length(pvalue)

# Ahora comprobamos que hemos hecho TODOS los cálculos
# Ejecutamos todo sin que de error.

# Hacemos un histograma de los p-values.
hist(pvalue)
hist(-log10(pvalue), col="brown")

# ¿Qué pasa si le ponemos con una transformación de -log10?
# que cambia la distribución de los datos

# Hacemos un volcano plot. Aquí podemos meter la diferencia de medias y la significancia estadística
Aquí podemos meter la diferencia de medias y la significancia estadística
plot(diff.mean, -log10(pvalue), main = "GSE5583 - Volcano")
#tendremos la diferencia de medias en contra del logaritmo.

# Queremos establecer que el mínimo para considerar una diferencia significativa, es con una diferencia de 2 y un p-value de 0.01
# ¿Puedes representarlo en el gráfico? #si

diff.mean_cutoff = 2
pvalue_cutoff = 0.01
abline(v = diff.mean_cutoff, col = "blue", lwd = 3)
#abline(v = -diff.mean_cutoff, col = "red", lwd = 3)
abline(h = -log10(pvalue_cutoff), col = "green", lwd = 3)

# Ahora buscamos los genes que satisfagan estos criterios
# Primero hacemos el filtro para la diferencia de medias (fold)
filter_by_diff.mean = abs(diff.mean) >= diff.mean_cutoff
dim(data[filter_by_diff.mean, ])

# Ahora el filtro de p-value
filter_by_pvalue = pvalue <= pvalue_cutoff
dim(data[filter_by_pvalue, ])

# Ahora las combinamos. ¿Cuántos genes cumplen los dos criterios? #426 genes
filter_combined = filter_by_diff.mean & filter_by_pvalue
filtered = data[filter_combined,]
dim(filtered)
head(filtered)

# Ahora generamos otro volcano plot con los genes seleccionados marcados en rojo
plot(diff.mean, -log10(pvalue), main = "GSE5583 - Volcano #2")
points (diff.mean[filter_combined], -log10(pvalue[filter_combined]),col = "red")

# Ahora vamos a marcar los que estarían sobreexpresados (rojo) y reprimidos (azul). ¿Por qué parece que están al revés?
# Porque hemos calculado la diferencia de medias al reves. Los reprimidos estarán en el lado positivo de la grafica y los sobreexpresados en el lado negativo.
plot(diff.mean, -log10(pvalue), main = "GSE5583 - Volcano #3")
points (diff.mean[filter_combined & diff.mean < 0],
	-log10(pvalue[filter_combined & diff.mean < 0]), col = "red")
points (diff.mean[filter_combined & diff.mean > 0],
	-log10(pvalue[filter_combined & diff.mean > 0]), col = "blue")

# Ahora vamos a generar un mapa. Para ello primero tenemos que hacer un cluster de las columnas y los genes 
# ¿Qué es cada parámetro que hemos usado dentro de la función heatmap?
       # filtered: es la matriz de datos que se utiliza para generar el heatmap. Debe contener los valores numéricos que se representarán en el gráfico.
	# Rowv=rowv: Se utiliza para especificar cómo se deben ordenar las filas en el heatmap.
	# Colv=colv: Se utiliza para especificar cómo se deben ordenar las columnas en el heatmap.
	# cexCol=0.7: Controla el tamaño de las etiquetas de las columnas en el heatmap. Un valor de 0.7 significa que las etiquetas de las columnas se reducirán al 70% de su tamaño normal.
	# labRow=FALSE: Controla si se deben mostrar etiquetas de fila en el heatmap. Al establecerlo en FALSE, se desactiva la visualización de las etiquetas de fila.

# ¿Eres capaz de cambiar los colores del heatmap? Pista: usar el argumento col y hcl.colors
rowv = as.dendrogram(hclust(as.dist(1-cor(t(filtered)))))
colv = as.dendrogram(hclust(as.dist(1-cor(filtered))))
heatmap(filtered, Rowv=rowv, Colv=colv, cexCol=0.7,labRow=FALSE)

